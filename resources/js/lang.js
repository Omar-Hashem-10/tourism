// ================================================================
//  getCurrentLang: read from <html lang=""> attribute
// ================================================================
export function getCurrentLang() {
    return document.documentElement.lang || 'ar';
}

// ================================================================
//  applyLang: update all [data-i18n] elements
// ================================================================
export function applyLang(lang) {
    const texts = TEXTS[lang] || TEXTS['ar'];

    document.documentElement.lang = lang;
    document.documentElement.dir  = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (texts[key] !== undefined) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = texts[key];
            } else {
                el.textContent = texts[key];
            }
        }
    });

    localStorage.setItem('lang', lang);

    // Update html lang attr for any dynamic re-renders
    document.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
}

// ================================================================
//  initLang: apply saved or default language on page load
// ================================================================
export function initLang(serverLang) {
    // Prefer server-set lang (from Laravel session), then localStorage, then 'ar'
    const saved = localStorage.getItem('lang');
    const lang  = serverLang || saved || 'ar';
    applyLang(lang);
    return lang;
}

// ================================================================
//  t: translate a single key, with optional %s substitution
// ================================================================
export function t(key, lang, ...args) {
    const texts = TEXTS[lang] || TEXTS['ar'];
    let str = texts[key] || key;
    args.forEach(arg => { str = str.replace('%s', arg); });
    return str;
}
